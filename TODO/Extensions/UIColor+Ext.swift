import UIKit

extension UIColor {
    
    class var dark: UIColor {
        return UIColor(red: 36.0 / 255.0, green: 28.0 / 255.0, blue: 60.0 / 255.0, alpha: 1.0)
    }
    
    class var darkSkyBlue: UIColor {
        return UIColor(red: 65.0 / 255.0, green: 104.0 / 255.0, blue: 233.0 / 255.0, alpha: 1.0)
    }
    
    class var bluishPurple: UIColor {
        return UIColor(red: 142.0 / 255.0, green: 42.0 / 255.0, blue: 234.0 / 255.0, alpha: 1.0)
    }
    
    class var fadedRed: UIColor {
        return UIColor(red: 226.0 / 255.0, green: 77.0 / 255.0, blue: 78.0 / 255.0, alpha: 1.0)
    }
}
